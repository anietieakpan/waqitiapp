package com.waqiti.rewards.enums;

/**
 * Referral Program Types
 *
 * Defines the different types of referral programs supported
 *
 * @author Waqiti Platform Team
 * @version 1.0
 * @since 2025-11-08
 */
public enum ProgramType {
    /**
     * Standard referral program with fixed rewards
     */
    STANDARD,

    /**
     * Tiered program with escalating rewards based on performance
     */
    TIERED,

    /**
     * Milestone-based program with achievement rewards
     */
    MILESTONE_BASED,

    /**
     * Time-limited promotional campaign
     */
    TIME_LIMITED,

    /**
     * Contest with winners and prizes
     */
    CONTEST,

    /**
     * Recurring monthly/seasonal program
     */
    RECURRING,

    /**
     * VIP/Exclusive invitation-only program
     */
    EXCLUSIVE
}
