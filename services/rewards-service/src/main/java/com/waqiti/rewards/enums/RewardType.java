package com.waqiti.rewards.enums;

/**
 * Reward Types for Referral Programs
 *
 * @author Waqiti Platform Team
 * @version 1.0
 * @since 2025-11-08
 */
public enum RewardType {
    /**
     * Loyalty points reward
     */
    POINTS,

    /**
     * Cashback reward (fixed amount)
     */
    CASHBACK,

    /**
     * Percentage-based reward (of transaction value)
     */
    PERCENTAGE,

    /**
     * Fixed monetary amount
     */
    FIXED_AMOUNT,

    /**
     * Bonus reward (special incentive)
     */
    BONUS,

    /**
     * Commission (percentage of revenue generated)
     */
    COMMISSION,

    /**
     * Voucher or coupon
     */
    VOUCHER,

    /**
     * Special offer or benefit
     */
    SPECIAL_OFFER,

    /**
     * Achievement badge
     */
    BADGE
}
